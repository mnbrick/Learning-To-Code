# calculator program, defining +-*/**

def add(a, b):
  return a + b
print (add(3, 5))

def subtract(a, b):
  return a - b
print (subtract(3, 5))

def multiply (a, b):
  return a * b
print (multiply(3, 5))

def divide(a, b):
  return a / b
print (divide(3, 5))

def exp(a, b):
  return a ** b
print(exp(3, 5))
